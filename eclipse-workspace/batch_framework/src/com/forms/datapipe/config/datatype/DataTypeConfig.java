package com.forms.datapipe.config.datatype;

import java.util.HashMap;
import java.util.Map;

/**
 * 对应配置文件database-input-config.xml
 * 
 * @author cxl
 * 
 */
public class DataTypeConfig
{
    private Map<String, DataType> dataTypes = new HashMap<String, DataType>();

    public void addDataType(DataType dataType)
    {
        dataTypes.put(dataType.getName(), dataType);
    }

    public Map<String, DataType> getDataTypes()
    {
        return dataTypes;
    }

    public void setDataTypes(Map<String, DataType> dataTypes)
    {
        this.dataTypes = dataTypes;
    }
}
