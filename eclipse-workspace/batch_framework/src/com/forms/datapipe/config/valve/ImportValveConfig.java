package com.forms.datapipe.config.valve;

import java.util.ArrayList;
import java.util.List;

/**
 * 对应配置文件import-valve-config.xml
 * 
 * @author cxl
 * 
 */
public class ImportValveConfig
{
    private List<ImportFieldMapping> fieldMappings = new ArrayList<ImportFieldMapping>();

    public void addFieldMapping(ImportFieldMapping fieldMapping)
    {
        fieldMappings.add(fieldMapping);
    }

    public List<ImportFieldMapping> getFieldMappings()
    {
        return fieldMappings;
    }

    public void setFieldMappings(List<ImportFieldMapping> fieldMappings)
    {
        this.fieldMappings = fieldMappings;
    }
}
