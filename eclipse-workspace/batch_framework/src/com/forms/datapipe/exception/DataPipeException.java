package com.forms.datapipe.exception;

public class DataPipeException
    extends Exception
{
    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public DataPipeException()
    {
        super();
    }

    public DataPipeException(String arg0, Throwable arg1)
    {
        super(arg0, arg1);
    }

    public DataPipeException(String arg0)
    {
        super(arg0);
    }

    public DataPipeException(Throwable arg0)
    {
        super(arg0);
    }

}
