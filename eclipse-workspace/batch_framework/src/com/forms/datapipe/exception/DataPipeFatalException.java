package com.forms.datapipe.exception;

public class DataPipeFatalException
    extends DataPipeException
{
    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public DataPipeFatalException()
    {
        super();
    }

    public DataPipeFatalException(String arg0, Throwable arg1)
    {
        super(arg0, arg1);
    }

    public DataPipeFatalException(String arg0)
    {
        super(arg0);
    }

    public DataPipeFatalException(Throwable arg0)
    {
        super(arg0);
    }

}
