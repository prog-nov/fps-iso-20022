package com.forms.framework.job.common.husservice;

public class HousekeepTable
{

	private String sourceTable;

	private String hisTable;

	private String jnlColumn;

	private String condition;

	private String id;

	public HousekeepTable(String sourceTable, String hisTable)
	{
		this.sourceTable = sourceTable;
		this.hisTable = hisTable;
	}

	public HousekeepTable()
	{
	}

	public String getHisTable()
	{
		return hisTable;
	}

	public void setHisTable(String hisTable)
	{
		this.hisTable = hisTable;
	}

	public String getSourceTable()
	{
		return sourceTable;
	}

	public void setSourceTable(String sourceTable)
	{
		this.sourceTable = sourceTable;
	}

	public String getCondition()
	{
		return condition;
	}

	public void setCondition(String condition)
	{
		this.condition = condition;
	}

	public String getJnlColumn()
	{
		return jnlColumn;
	}

	public void setJnlColumn(String jnlColumn)
	{
		this.jnlColumn = jnlColumn;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	
}
