public abstract class AbstractSqlConfigFunction implements ISqlConfigFunction {

    private int order;

    private String name;

    /**
     * 默认单例
     */
    @Override
    public boolean isSingleon() {
        return true;
    }

    public int getOrder() {
        return order;
    }

    public void setOrder(int order) {
        this.order = order;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
