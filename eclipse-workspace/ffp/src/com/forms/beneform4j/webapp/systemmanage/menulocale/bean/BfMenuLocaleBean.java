public class BfMenuLocaleBean implements Serializable {

    private static final long serialVersionUID = 3128136017756095642L;

    /**
     * BF_MENU_I18N.MENU_ID 菜单ID
     */
    private int menuId;

    /**
     * BF_MENU_I18N.LOCALE 地区
     */
    private String locale;

    /**
     * BF_MENU_I18N.MENU_NAME 菜单名称
     */
    private String menuName;

    /**
     * BF_MENU_I18N.DES 菜单描述
     */
    private String des;

    public int getMenuId() {
        return menuId;
    }

    public void setMenuId(int menuId) {
        this.menuId = menuId;
    }

    public String getLocale() {
        return locale;
    }

    public void setLocale(String locale) {
        this.locale = locale;
    }

    public String getMenuName() {
        return menuName;
    }

    public void setMenuName(String menuName) {
        this.menuName = menuName;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }
}
