package com.forms.ffp.persistents.bean;

import java.util.ArrayList;
import java.util.List;

public class FFPJbBase
{
	private FFPTxJnl txJnl;
	private List<FFPTxJnlAction> jnlActionList;

	public FFPTxJnl getTxJnl()
	{
		if (txJnl == null)
		{
			txJnl = new FFPTxJnl();
		}
		return txJnl;
	}

	public void setTxJnl(FFPTxJnl txJnl)
	{
		this.txJnl = txJnl;
	}

	public List<FFPTxJnlAction> getJnlActionList()
	{
		if (jnlActionList == null)
		{
			jnlActionList = new ArrayList<FFPTxJnlAction>();
		}
		return jnlActionList;
	}

	public void setJnlActionList(List<FFPTxJnlAction> jnlActionList)
	{
		this.jnlActionList = jnlActionList;
	}

}
