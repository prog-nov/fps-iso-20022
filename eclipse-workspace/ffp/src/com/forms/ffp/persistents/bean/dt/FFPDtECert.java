package com.forms.ffp.persistents.bean.dt;

import java.util.Date;

public class FFPDtECert
{
	private int id;

	private String systemId;

	private String eCertKey;

	private Date effectiveDate;

	private Date expiryDate;

	private Date RevokedTs;

	public int getId()
	{
		return id;
	}

	public void setId(int id)
	{
		this.id = id;
	}

	public String getSystemId()
	{
		return systemId;
	}

	public void setSystemId(String systemId)
	{
		this.systemId = systemId;
	}

	public String geteCertKey()
	{
		return eCertKey;
	}

	public void seteCertKey(String eCertKey)
	{
		this.eCertKey = eCertKey;
	}

	public Date getEffectiveDate()
	{
		return effectiveDate;
	}

	public void setEffectiveDate(Date effectiveDate)
	{
		this.effectiveDate = effectiveDate;
	}

	public Date getExpiryDate()
	{
		return expiryDate;
	}

	public void setExpiryDate(Date expiryDate)
	{
		this.expiryDate = expiryDate;
	}

	public Date getRevokedTs()
	{
		return RevokedTs;
	}

	public void setRevokedTs(Date revokedTs)
	{
		RevokedTs = revokedTs;
	}

}
