package com.forms.ffp.persistents.bean.ss;

import java.util.Date;

public class FFPSsCutoff
{
	private String cutoffType;

	private Date workdayStart;

	private Date workdayEnd;

	private Date satStart;

	private Date satEnd;

	private Date holidayStart;

	private Date holidayEnd;

	public String getCutoffType()
	{
		return cutoffType;
	}

	public void setCutoffType(String cutoffType)
	{
		this.cutoffType = cutoffType;
	}

	public Date getWorkdayStart()
	{
		return workdayStart;
	}

	public void setWorkdayStart(Date workdayStart)
	{
		this.workdayStart = workdayStart;
	}

	public Date getWorkdayEnd()
	{
		return workdayEnd;
	}

	public void setWorkdayEnd(Date workdayEnd)
	{
		this.workdayEnd = workdayEnd;
	}

	public Date getSatStart()
	{
		return satStart;
	}

	public void setSatStart(Date satStart)
	{
		this.satStart = satStart;
	}

	public Date getSatEnd()
	{
		return satEnd;
	}

	public void setSatEnd(Date satEnd)
	{
		this.satEnd = satEnd;
	}

	public Date getHolidayStart()
	{
		return holidayStart;
	}

	public void setHolidayStart(Date holidayStart)
	{
		this.holidayStart = holidayStart;
	}

	public Date getHolidayEnd()
	{
		return holidayEnd;
	}

	public void setHolidayEnd(Date holidayEnd)
	{
		this.holidayEnd = holidayEnd;
	}

}
