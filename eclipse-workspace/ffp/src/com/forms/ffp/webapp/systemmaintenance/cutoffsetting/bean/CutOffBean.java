package com.forms.ffp.webapp.systemmaintenance.cutoffsetting.bean;

public class CutOffBean {
	
	private String cutoffType;
	
	private String workdayStart;
	
	private String workdayEnd;
	
	private String satStart;
	
	private String satEnd;
	
	private String holidayStart;
	
	private String holidayEnd;

	public String getCutoffType() {
		return cutoffType;
	}

	public void setCutoffType(String cutoffType) {
		this.cutoffType = cutoffType;
	}
	
	public String getWorkdayStart() {
		return workdayStart;
	}

	public void setWorkdayStart(String workdayStart) {
		this.workdayStart = workdayStart;
	}

	public String getWorkdayEnd() {
		return workdayEnd;
	}

	public void setWorkdayEnd(String workdayEnd) {
		this.workdayEnd = workdayEnd;
	}

	public String getSatStart() {
		return satStart;
	}

	public void setSatStart(String satStart) {
		this.satStart = satStart;
	}

	public String getSatEnd() {
		return satEnd;
	}

	public void setSatEnd(String satEnd) {
		this.satEnd = satEnd;
	}

	public String getHolidayStart() {
		return holidayStart;
	}

	public void setHolidayStart(String holidayStart) {
		this.holidayStart = holidayStart;
	}

	public String getHolidayEnd() {
		return holidayEnd;
	}

	public void setHolidayEnd(String holidayEnd) {
		this.holidayEnd = holidayEnd;
	}
	
	

}
