public interface IParamExtractor {

    /**
     * 根据类型、名称从参数包装对象中提取方法调用参数
     * 
     * @param type 目标类型
     * @param expression 提取参数的表达式
     * @param args 参数对象
     * @return 提取的参数值
     */
    public <E> E extract(Class<E> type, String expression, Object args);
}
